package org.example;

public class ConversaoDeDados {
    public static double convKmmt(double quilometro) {
        return quilometro * 1000;
    }

    public static double convLDl(double litro) {
        return litro * 10;
    }

    public static double convMcPc(double metroCubico) {
        return metroCubico * 35.31;
    }

    public static double convBlDl(double barril) {
        return barril * 16.36;
    }

    public static double convBlL(double barril) {
        return barril * 163.65;
    }
}

